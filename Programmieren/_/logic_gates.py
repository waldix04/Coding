def AND (a,b):
    if a==1 and b==1:
        return True
    else:
        return False
print(AND(1,1))

def OR (a,b):
    if a==1 or b==1:
        return True
    else:
        return False
print(OR(0,0))

def NOT (a):
    if a==1:
        return not True
    else:
        return not False
print(NOT(0))



